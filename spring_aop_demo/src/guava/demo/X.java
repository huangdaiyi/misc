package guava.demo;

public class X {
	private String a;
	private String b;
	private String v;
	private String w;
	private String m;
	private String n;

	public String getA() {
		return a;
	}

	public void setA(String a) {
		this.a = a;
	}

	public String getB() {
		return b;
	}

	public void setB(String b) {
		this.b = b;
	}

	public String getV() {
		return v;
	}

	public void setV(String v) {
		this.v = v;
	}

	public String getW() {
		return w;
	}

	public void setW(String w) {
		this.w = w;
	}

	public String getM() {
		return m;
	}

	public void setM(String m) {
		this.m = m;
	}

	public String getN() {
		return n;
	}

	public void setN(String n) {
		this.n = n;
	}

	public X(String a, String b, String v, String w, String m, String n) {
		this.a = a;
		this.b = b;
		this.v = v;
		this.w = w;
		this.m = m;
		this.n = n;
	}
}
