package guava.demo;


public class Y {
	public Y(String a, String b) {
		super();
		this.a = a;
		this.b = b;
	}

	private String a;
	private String b;

	public String getA() {
		return a;
	}

	public void setA(String a) {
		this.a = a;
	}

	public String getB() {
		return b;
	}

	public void setB(String b) {
		this.b = b;
	}
}